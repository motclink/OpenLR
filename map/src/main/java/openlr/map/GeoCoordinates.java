package openlr.map;


/**
 * The interface GeoCoordinates defines a coordinate pair of a longitude and a
 * latitude value.
 *
 * <p>
 * OpenLR is a trade mark of TomTom International B.V.
 * <p>
 * email: software@openlr.org
 *
 * @author TomTom International B.V.
 */
public interface GeoCoordinates {

    /**
     * Delivers the degrees of longitude.
     *
     * @return the longitude
     */
    double getLongitudeDeg();

    /**
     * Delivers the degrees of latitude.
     *
     * @return the latitude
     */
    double getLatitudeDeg();

    /**
     * {@inheritDoc}
     */
    @Override
    int hashCode();

    /**
     * {@inheritDoc}
     */
    @Override
    boolean equals(final Object obj);
}
