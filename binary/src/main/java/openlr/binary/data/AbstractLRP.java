package openlr.binary.data;

/**
 * Base class for location reference points (LRP). Each LRP stores at least
 * a coordinate pair (lat/lon) and a first attribute consisting of a functional
 * road class and a form of way value.
 *
 * <p>
 * OpenLR is a trade mark of TomTom International B.V.
 * <p>
 * email: software@openlr.org
 *
 * @author TomTom International B.V.
 *
 */
public abstract class AbstractLRP extends AbstractCoordinate {


    /** The attrib1 information. */
    protected Attr1 attrib1;

    /**
     * Instantiates a new abstract lrp.
     *
     * @param coordBits the coord bits
     */
    public AbstractLRP(final int coordBits) {
        super(coordBits);
    }


    /**
     * Gets the attribute 1 information.
     *
     * @return the attrib1
     */
    public final Attr1 getAttrib1() {
        return attrib1;
    }

}
